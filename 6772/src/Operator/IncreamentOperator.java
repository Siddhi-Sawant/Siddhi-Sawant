package Operator;

public class IncreamentOperator 
{

	public static void main(String[] args) 
	{
		int a=10;
		int b=5;
		System.out.println("Value of a: "+a);
		System.out.println("Value of b :"+b);
		System.out.println("Value of ++a: "+(++a));
		System.out.println("Value of a: "+a);
		System.out.println("Value of b++: "+(b++));
		System.out.println("Value of b: "+b);

	}

}
